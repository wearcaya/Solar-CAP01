#!/usr/bin/env python3
"""
Plot results generated by run_experiments_preprocessed.py
"""
from pathlib import Path
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

ROOT = Path(__file__).resolve().parent
RES_DIR = ROOT.parent / "results" / "preprocessed_csv"
FIG_DIR = RES_DIR / "figures"
FIG_DIR.mkdir(parents=True, exist_ok=True)

# Load data
summary = pd.read_csv(RES_DIR / "preprocessed_summary.csv")
hourly = pd.read_csv(RES_DIR / "preprocessed_hourly.csv")

policies = summary['policy'].unique().tolist()

# 1) Energy dynamics for Solar-CAP
rep = hourly[hourly['policy']=='Solar-CAP'].copy()
if not rep.empty:
    fig, ax1 = plt.subplots(figsize=(8,4))
    ax1.plot(rep['hour'], rep['solar_mean_kwh'], marker='o', label='Mean solar harvest')
    if 'battery_mean_kwh' in rep.columns:
        ax1.plot(rep['hour'], rep['battery_mean_kwh'], marker='s', label='Mean battery level')
    ax1.set_xlabel('Hour of day')
    ax1.set_ylabel('kWh per node')
    ax1.set_xticks(range(0,24,2))
    ax2 = ax1.twinx()
    if 'brown_total_kwh' in rep.columns:
        ax2.bar(rep['hour'], rep['brown_total_kwh'], alpha=0.25, label='Brown energy')
        ax2.set_ylabel('Brown energy (kWh)')
    lines1, labels1 = ax1.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    ax1.legend(lines1+lines2, labels1+labels2, loc='upper left')
    ax1.set_title('Solar-CAP energy dynamics')
    fig.tight_layout()
    fig.savefig(FIG_DIR / 'energy_dynamics.png', dpi=200)
    plt.close(fig)

# 2) Activation and migration timeline (Solar-CAP)
if not rep.empty:
    fig, ax1 = plt.subplots(figsize=(8,3))
    ax1.step(rep['hour'], rep['active_nodes'], where='mid', linewidth=2, label='Active nodes')
    ax1.set_xlabel('Hour of day')
    ax1.set_ylabel('Active nodes')
    ax1.set_xticks(range(0,24,2))
    ax2 = ax1.twinx()
    ax2.bar(rep['hour'], rep['migrations'], alpha=0.25, label='Migrations')
    ax2.set_ylabel('Migrations')
    l1, lab1 = ax1.get_legend_handles_labels()
    l2, lab2 = ax2.get_legend_handles_labels()
    ax1.legend(l1+l2, lab1+lab2, loc='upper right')
    ax1.set_title('Activation and migrations (Solar-CAP)')
    fig.tight_layout()
    fig.savefig(FIG_DIR / 'activation_migration.png', dpi=200)
    plt.close(fig)

# 3) Emissions by policy (bar)

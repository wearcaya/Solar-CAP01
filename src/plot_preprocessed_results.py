plt.close(fig)

# 4) Migrations by policy
fig, ax = plt.subplots(figsize=(6,4))
summary.plot(x='policy', y='migrations', kind='bar', ax=ax, legend=False)
ax.set_ylabel('Migrations')
ax.set_title('Migrations by policy')
fig.tight_layout()
fig.savefig(FIG_DIR / 'migrations_by_policy.png', dpi=200)
plt.close(fig)

# 5) Reduction heatmap: compute reductions of Solar-CAP vs Always-on and Static-K and Greedy
base = summary.set_index('policy')
if 'Solar-CAP' in base.index:
    sc = base.loc['Solar-CAP','emissions_kgco2eq']
    reductions = {}
    for pol in ['Always-on','Static-K','Greedy carbon-aware']:
        if pol in base.index:
            reductions[pol] = 100*(1 - sc / base.loc[pol,'emissions_kgco2eq'])
    # Simple bar
    fig, ax = plt.subplots(figsize=(6,3))
    ax.bar(list(reductions.keys()), list(reductions.values()))
    ax.set_ylabel('Reduction (%)')
    ax.set_title('Solar-CAP reduction vs baselines')
    for i,(k,v) in enumerate(reductions.items()):
        ax.text(i, v+1, f"{v:.1f}%", ha='center')
    fig.tight_layout()
    fig.savefig(FIG_DIR / 'reduction_vs_baselines.png', dpi=200)
    plt.close(fig)

print('Figures saved to', FIG_DIR)
